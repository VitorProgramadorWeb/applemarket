        <h1>Display</h1>
    
        <p>A propriedade <span>display</span> define se o elemento é tratado como um elemento em bloco ou em linha e o layout de fluxo usado para os seus filhos.</p>
    
        <h2>Flow Layout</h2>
        <ul>
            <li><span class="destaque">Inline:</span> São elementos dispostos em linha, ou seja, de acordo como as palavras estão dispostas dentro do parágrafo, uma na frente da outra. Vão da direita para a esquerda.</li>
            <li><span class="destaque">Block:</span> São elementos dispostos em bloco, um em cima do outro, assim como os parágrafos e títulos. Vão de cima para baixo.</li>
        </ul>
